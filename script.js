const container = document.createElement('div');
container.classList.add('container');
document.body.append(container);

const textarea = document.createElement('textarea');
textarea.classList.add('textarea');
container.append(textarea);
textarea.focus();

const keyboard = document.createElement('div');
keyboard.classList.add('keyboard');
container.append(keyboard);

if (!localStorage.language) localStorage.language = 'EN';

function createKeyboard() {
  localStorage.getItem('language');
  keys.forEach((row, index) => {
    const keysRow = document.createElement('div');
    keyboard.append(keysRow);
    keysRow.classList = `keys-row keys-row${index}`;

    row.forEach((item) => {
      const key = document.createElement('span');
      keysRow.append(key);
      key.classList = `key key${item.style}`;

      if (item.en || item.ru) {
        if (localStorage.getItem('language') === 'RU') {
          key.innerHTML = item.ru;
        } else {
          key.innerHTML = item.en;
        }
      } else {
        key.innerHTML = item.title;
      }
      key.id = item.keyCode;
    });
  });
}

createKeyboard();

const buttons = document.querySelectorAll('.key');
const capsLockBtn = document.getElementById('20');

function keyPress() {
  container.addEventListener('keydown', (event) => {
    textarea.focus();

    buttons.forEach((elem) => {
      if (elem.id == event.keyCode) {
        if (elem === capsLockBtn) {
          elem.classList.toggle('press');
        } else {
          elem.classList.add('press');
        }
      }
    });
  });

  container.addEventListener('keyup', () => {
    buttons.forEach((elem) => {
      if (elem !== capsLockBtn) {
        elem.classList.remove('press');
      }
    });
  });
}

keyPress();

let text = '';
const keyboardContainer = document.querySelector('.keyboard');

keyboardContainer.addEventListener('mousedown', (event) => {
  const btn = event.target;

  if (btn) {
    text += btn.innerText;
    document.querySelector('.textarea').innerText = text;
  }
});
